#include <unistd.h>

void	ft_retenue(char *a)
{
	a[4] = a[4] + 1;
	if (a[4] > '9')
	{
		a[4] = '0';
		a[3] = a[3] + 1;
	}
	if (a[3] > '9')
	{
		a[3] = '0';
		a[1] = a[1] + 1;
	}
	if (a[1] > '9')
	{
		a[1] = '0';
		a[0] = a[0] + 1;
	}
}

void	ft_print_comb2(void)
{
	char	r[7];
	char	*pointeur;
	int		i;

	i = 0;
	pointeur = &r[0];
	r[0] = '0';
	r[1] = '0';
	r[2] = ' ';
	r[3] = '0';
	r[4] = '0';
	r[5] = ',';
	r[6] = ' ';
	while (i <= 10000)
	{
		if ((r[0] < r[3]) || (r[0] == r[3] && r[1] < r[4]))
		{
			if (r[0] == '9' && r[1] == '8')
				write(1, &"98 99", 5);
			else
				write(1, &r, 7);
		}
		ft_retenue(pointeur);
		i = i + 1;
	}
}
