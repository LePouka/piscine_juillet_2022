#include <unistd.h>

void	ft_print_comb(void)
{
	int	a;
	int	s;
	int	d;

	a = '0';
	while (a < '8')
	{
		s = a + 1;
		while (s < '9')
		{
			d = s + 1;
			while (d <= '9')
			{	
				write(1, &a, 1);
				write(1, &s, 1);
				write(1, &d, 1);
				if (a != '7' || s != '8' || d != '9')
					write(1, ", ", 2);
				d++;
			}
			s++;
		}
		a++;
	}
}
